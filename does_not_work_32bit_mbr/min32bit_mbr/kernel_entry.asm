; =================================================================
; ФАЙЛ: kernel_entry.asm (Точка входа 32-бит)
; =================================================================
BITS 32

global _start
extern kernel_main

section .text.entry
_start:
    ; Инициализация 32-битного стека 
    mov esp, 0x90000 
    
    ; Вызов основной функции ядра C
    call kernel_main

    ; Зависание после выполнения
    jmp $