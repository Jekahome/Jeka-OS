; =================================================================
; ФАЙЛ: loader_stage1.asm
; Описание: Минимальный загрузчик, который читает следующий сектор
;           (ядро) в память (0x10000), а затем зависает.
;
; Рабочий:
; Теперь, когда ядро загружено и ждет, пришло время для финального 
; шага — перехода в 32-битный Защищенный Режим (Protected Mode) и 
; передачи управления коду ядра.
; =================================================================

[org 0x7C00]
BITS 16

start:
    ; 1. Инициализация сегментных регистров
    cli
    xor ax, ax
    mov ds, ax
    mov es, ax
    mov ss, ax
    mov sp, 0x7C00

    ; 2. Вывод сообщения о начале загрузки
    mov si, msg_loading
    call print_string

    ; =============================
    ; 3. ЧТЕНИЕ ЯДРА (СЕКТОР 2)
    ; =============================
    
    ; Установка адреса в памяти, куда будет загружено ядро (0x1000:0x0000 = 0x10000)
    mov bx, 0x1000      ; Сегмент 0x1000
    mov es, bx          ; ES = 0x1000
    mov bx, 0x0         ; BX = 0x0000. Конечный адрес: ES:BX = 0x10000
    
    mov ah, 0x02        ; Функция: Чтение секторов с диска (Int 13h)
    mov al, 1           ; Читать 1 сектор
    mov ch, 0           ; Цилиндр 0
    mov cl, 2           ; Сектор 2 (Ядро начинается со второго сектора)
    mov dh, 0           ; Головка 0
    mov dl, 0x00        ; Номер диска (80h = A: drive/первый HDD), (00h = A: Floppy drive)
    
    int 0x13            ; Вызов BIOS для чтения
    
    jc disk_error       ; Если чтение не удалось (Carry Flag установлен), переходим к ошибке

    ; 4. Вывод сообщения об успешной загрузке
    mov si, msg_success
    call print_string

    ; 5. Конец: Просто зависаем, пока не добавим Protected Mode
    jmp $

disk_error:
    mov si, msg_error
    call print_string
    jmp $

; -------------------------------------------
; Подпрограмма вывода строки (использует INT 10h)
; -------------------------------------------
print_string:
    mov ah, 0x0E    ; Функция Teletype output (BIOS int 0x10)
.loop:
    lodsb           ; Загрузить байт из [si] в AL, увеличить SI
    or al, al       ; Сравнить AL с нулём (null-terminator)
    jz .done        ; Если равно нулю, завершить
    int 0x10        ; Вызвать прерывание BIOS
    jmp .loop
.done:
    ret

; -------------------------------------------
; Данные
; -------------------------------------------
msg_loading db "Reading kernel (sector 2)...", 0xA, 0xD, 0 ; 0xA, 0xD = перевод строки
msg_success db "Kernel read successful! Waiting for 32-bit jump...", 0xA, 0xD, 0
msg_error db "DISK READ ERROR!", 0xA, 0xD, 0

; Заполнение и MBR сигнатура
times 510 - ($ - $$) db 0
dw 0xAA55